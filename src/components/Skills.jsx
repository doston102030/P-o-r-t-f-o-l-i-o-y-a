export default function Skills() {
  const { t } = useLanguage();
  const [skills, setSkills] = useState([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    getDoc(doc(db, 'portfolio', 'skills')).then(snap => {
      if (snap.exists() && snap.data().items) {
        setSkills(snap.data().items);
      } else {
        setSkills(defaultSkills);
      }
      setLoading(false);
    }).catch(err => {
      console.error("Firestore error:", err);
      setSkills(defaultSkills);
      setLoading(false);
    });
  }, []);

  if (loading) return <section className="section skills-section" id="skills" style={{ minHeight: '400px' }} />;

  return (
    <section className="section skills-section" id="skills">
      <div className="skills-bg-pattern" />

      <div className="container">
        <div className="section-header animate-fade-up">
          <p className="section-tag">{t('skills.tag')}</p>
          <h2 className="section-title">
            {t('skills.title')} <span className="gradient-text">{t('skills.titleGradient')}</span>
          </h2>
          <p className="section-subtitle">
            {t('skills.subtitle')}
          </p>
        </div>

        <div className="skills-grid animate-fade-up delay-2">
          {skills.map((skill, i) => (
            <div
              key={skill.name}
              className="skill-card"
              style={{ '--skill-color': skill.color, animationDelay: `${i * 0.05}s` }}
            >
              <div className="skill-icon-wrapper">
                <div className="skill-svg-icon" style={{ color: skill.color }}>
                  {svgIcons[skill.name]}
                </div>
              </div>
              <div className="skill-info">
                <span className="skill-name">{skill.name}</span>
                <div className="skill-bar">
                  <div
                    className="skill-bar-fill"
                    style={{ '--width': `${skill.level}%`, background: skill.color }}
                  />
                </div>
                <span className="skill-level">{skill.level}%</span>
              </div>
              <div className="skill-glow" />
            </div>
          ))}
        </div>
      </div>
    </section>
  );
}
